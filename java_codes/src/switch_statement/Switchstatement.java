package switch_statement;

public class Switchstatement {

	public static void main(String[] args) {
		int n= 1;
		switch(n){
			case 1:
				System.out.println("case1");
				break;
			case 2:
				System.out.println("case2");
				break;
				
			default:
				System.out.println("enter the valid numbers");
				
		}

	}

}
